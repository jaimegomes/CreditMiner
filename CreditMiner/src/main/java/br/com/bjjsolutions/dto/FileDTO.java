package br.com.bjjsolutions.dto;

public class FileDTO {

	private String fileName;

	public FileDTO() {
		super();
	}

	/**
	 * @return the fileName
	 */
	public String getFileName() {
		return fileName;
	}

	/**
	 * @param fileName
	 *            the fileName to set
	 */
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

}
